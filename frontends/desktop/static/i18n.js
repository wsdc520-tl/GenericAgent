  for (const [code, messages] of Object.entries(BOOT_I18N)) {
    I18N[code] = Object.assign(I18N[code] || {}, messages);
  }

  const LANGS = Object.freeze(Object.keys(I18N));

  function normalizeLang(code) {
    return LANGS.includes(code) ? code : 'zh';
  }

  function browserLang() {
    return (navigator.language || '').toLowerCase().startsWith('zh') ? 'zh' : 'en';
  }

  function bootLang() {
    try {
      const saved = localStorage.getItem('ga_lang');
      if (LANGS.includes(saved)) return saved;
    } catch (_) {}
    return browserLang();
  }

  function tFor(lang, key, vars) {
    const active = normalizeLang(lang);
    let text = (I18N[active] && I18N[active][key]) || (I18N.zh && I18N.zh[key]) || (I18N.en && I18N.en[key]) || key;
    if (vars) {
      for (const [name, value] of Object.entries(vars)) {
        text = text.replaceAll(`{${name}}`, String(value));
      }
    }
    return text;
  }

  function apply(root, activeLang) {
    const lang = normalizeLang(activeLang || bootLang());
    const scope = root || document;
    if (scope === document) document.documentElement.lang = lang === 'en' ? 'en' : 'zh-CN';
    scope.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = tFor(lang, el.dataset.i18n);
    });
    scope.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.setAttribute('data-ph', tFor(lang, el.dataset.i18nPh));
    });
    scope.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.setAttribute('placeholder', tFor(lang, el.dataset.i18nPlaceholder));
    });
    scope.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.setAttribute('title', tFor(lang, el.dataset.i18nTitle));
      el.setAttribute('aria-label', tFor(lang, el.dataset.i18nTitle));
    });
  }

  window.GA_I18N = {
    dict: I18N,
    languages: LANGS,
    t: tFor,
    apply,
    bootLang,
    browserLang,
  };

  window.GA_BOOT_I18N = {
    get lang() { return bootLang(); },
    t(key, vars) { return tFor(bootLang(), key, vars); },
    apply(root) { return apply(root || document, bootLang()); },
  };
